package model;

import java.sql.Date;

/**
 *
 * @author namdh
 */
public class Student {
    private int sid;
    private String sname;
    private boolean gender;
    private Date dob;
    private int cid;

    public Student() {
    }

    public Student(int sid, String sname, boolean gender, Date dob, int cid) {
        this.sid = sid;
        this.sname = sname;
        this.gender = gender;
        this.dob = dob;
        this.cid = cid;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }
    
    
}
